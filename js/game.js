var Direction = 
{
    // None:       0,
    Left:       0,
    Right:      1,
    Up:         2,
    Down:       3
};
Direction.opposite = function(dir)
{
    switch (dir)
    {
     case Direction.Left:
        return Direction.Right;
     case Direction.Right:
        return Direction.Left;
     case Direction.Up:
        return Direction.Down;
     case Direction.Down:
        return Direction.Up;
    }
};

